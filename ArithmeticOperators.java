class ArithmeticOperators{

    public static void main(String args[]){

        int num1 = 15;
        int num2 = 40;

        System.out.println(num1);
        System.out.println(num1++);
        System.out.println(num1);


        System.out.println(num2);
        System.out.println(++num2);
        System.out.println(num2);

        //arithmetic => +,-,*,/,%
        //relational => >,<,!<,>=,<=
        //logical => and(&&), or(||), not(!)


    }
}