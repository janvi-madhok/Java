class Ifelseif{

    public static void main(String args[]){

        int x = 20;
        int y = 198;
        int z = 1200;

        if(x>y && x>z)
            System.out.println(x);
        else if(y>z)
            System.out.println(y);
        else
            System.out.println(z);
    }
}